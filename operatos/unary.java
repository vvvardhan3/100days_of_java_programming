package operatos;

public class unary {
    public static void main(String[] args) {
        int i = 10;
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i++);
        System.out.println(i);

        System.out.println(" ");

        int j = 10;
        System.out.println(j);
        System.out.println(--j);
        System.out.println(j--);
        System.out.println(j);


        System.out.println(" ");

        int k = 10;
        System.out.println(k);
        System.out.println(++k);
        System.out.println(k--);
        System.out.println(k);
        System.out.println(k++);
        System.out.println(k);
        System.out.println(k--);
        System.out.println(k);
    }
}
